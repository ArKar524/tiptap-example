    /**
     * Store a newly created resource in storage.
     */
    public function store(Request $request)
    {
        // dd($request->all());
        $request->validate([
            'title' => 'required',
            'content' => 'required|min:1',
        ]);
        Post::create([
            'title' => $request->title,
            'content' => $request->content
        ]);
        return back();
    }

